
  <h2>About KeralaJS</h2>
  <p>KeralaJS is a JavaScript user group and developer community from God's own country. Our goal is to *be* the platform for JavaScript and front-end 
  developers to connect, network and share their experiences. We'll have monthly meetups and occational events mainly focusing on
  JavaScript and different front-end technologies. Developers, JavaScript enthusiasts and Students are welcome!</p>
  

